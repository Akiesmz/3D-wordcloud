import jieba
from collections import Counter
import re

STOPWORDS = {
    "的", "了", "在", "是", "我", "有", "和", "就", "不", "人",
    "都", "一", "一个", "上", "也", "很", "到", "说", "要", "去",
    "你", "会", "着", "没有", "看", "好", "自己", "这", "那",
    "他", "她", "它", "们", "把", "被", "让", "给", "对", "但",
    "而", "或", "与", "及", "等", "之", "于", "为", "以", "从",
    "可以", "这个", "那个", "什么", "怎么", "如何", "为什么",
    "啊", "呢", "吧", "吗", "呀", "哦", "哈", "嗯", "哇",
    "还", "只", "只是", "已经", "又", "再", "才", "就是", "所以",
    "因为", "如果", "虽然", "但是", "然而", "而且", "或者",
    "其", "此", "某", "每", "各", "某", "任何", "所有", "一些",
    "然后", "以后", "之前", "当时", "现在", "今天", "明天",
    "中", "内", "外", "里", "间", "前", "后", "左", "右",
    "大", "小", "多", "少", "些", "最", "更", "非常", "特别",
    "太", "真", "挺", "蛮", "相当", "比较", "极", "甚", "尤",
    "没", "无", "非", "别", "莫", "勿", "甭", "unix", "windows"
}

def tokenize_and_count(text: str) -> Counter:
    text = re.sub(r'[^\u4e00-\u9fa5a-zA-Z0-9]', ' ', text)
    
    words = jieba.cut(text)
    
    filtered_words = [
        word.strip().lower() 
        for word in words 
        if len(word.strip()) >= 2 and word.strip() not in STOPWORDS
    ]
    
    return Counter(filtered_words)
